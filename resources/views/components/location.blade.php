    <script>
        // Location tab functionality
        function showLocation(type) {
            // Update tab styles
            const tabs = document.querySelectorAll('.location-tab');
            tabs.forEach(tab => {
                tab.classList.remove('active', 'bg-pink-500', 'text-white');
                tab.classList.add('bg-gray-200', 'text-gray-700');
            });
            
            // Add active class to clicked tab
            event.target.classList.add('active', 'bg-pink-500', 'text-white');
            event.target.classList.remove('bg-gray-200', 'text-gray-700');
            
            // Show/hide location info
            const akadInfo = document.getElementById('akad-info');
            const resepsiInfo = document.getElementById('resepsi-info');
            
            if (type === 'akad') {
                akadInfo.classList.remove('hidden');
                resepsiInfo.classList.add('hidden');
            } else {
                akadInfo.classList.add('hidden');
                resepsiInfo.classList.remove('hidden');
            }
        }
    </script>
